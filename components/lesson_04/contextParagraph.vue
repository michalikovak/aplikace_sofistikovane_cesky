<p>
  Když začala J.K. Rowlingová psát příběhy o Harry Potterovi a jeho kamarádech,
  sama netušila jaký poprask tím vyvolá. Prodávanost jejích knih se stala
  <strong>bezprecedentní</strong> a vytvořila dlouho nepřekonatelný rekord. To,
  jak dokázala mistrně <strong>fabulovat</strong> děj tohoto vyprávění, kdy
  čtenář nedokáže knihu odložit, je až dech beroucí. Jedině nějaký
  <strong>analfabet</strong> by nedokázal ocenit, co tato žena dokázala. Zrodila
  něco neskutečného, zrodila Harryho Pottera. Je možné, že řada čtenářů má
  <strong>obsedantní</strong> názor, že Harry zneužívá toho, že se stal už za
  svého života legendou. Někomu se také může jevit jako
  <strong>egoistický</strong> chlapec jednající pouze ve vlastním zájmu, který
  je kvůli svému původy poněkud <strong>ješitný</strong>, ale opak je pravdou.
  Harry je týmový hráč. Samozřejmě, Harry by nebyl Harrym, nebýt jeho přátel.
  Vzpomeňme například na <strong>žoviálního</strong> Hagrida, který sice rozumu
  moc nepobral, ale jeho srdce je na tom správném místě. Nebo na Hermionu
  Grangerovou, která bývá na své přátele poněkud <strong>sarkastická</strong>,
  ale to je tím, že je premiantkou a své kamarády převyšuje intelektem. Harry
  ovšem nemá jen samé přátele. Jistě si ještě pamatujete na šestý díl, kde ho ve
  vlaku do Bradavic <strong>inzultoval</strong> Draco Malfloy. Může se zdát, že
  jsem poněkud <strong>patetický</strong>, ale knihy o mladém čaroději mě zcela
  pohltily a já je přečetl jedním dechem.
</p>
